    public void loadXuatKho()
    {
        try {
            ArrayList<XuatKho> dsxk = XuatKhoDAO.layDsXuatKho();
            dtm.setRowCount(0);
            for(XuatKho nk : dsxk)
            {
                Vector<Object> vec = new Vector<Object>();
                vec.add(nk.getMaXuatKho());
                vec.add(nk.getMaNguyenLieu());
                vec.add(nk.getNgayXuat());
                vec.add(nk.getSoLuong());
                vec.add(nk.getThanhTien());
                dtm.addRow(vec);
            }
            tbXuatKho.setModel(dtm);
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }

