    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if(tfMaXuatKho.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(null, "Mã nhập kho không được để trống","Lỗi",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String ma = tfMaXuatKho.getText().trim();
        if(XuatKhoDAO.ktMa(ma))
        {
            int n = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn update?","Thông báo",JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if(n == JOptionPane.YES_OPTION)
            {
                XuatKho xk = new XuatKho();
                xk.setMaXuatKho(tfMaXuatKho.getText().trim());
                xk.setMaNguyenLieu(tfMaNL.getText().trim());
                xk.setNgayXuat(Date.valueOf(tfNgayXuat.getText().trim()));
                xk.setSoLuong(Integer.parseInt(tfSoLuong.getText()));
                xk.setThanhTien(Integer.parseInt(tfThanhTien.getText()));
                boolean kq = XuatKhoDAO.capNhatXuatKho(xk);
                if(kq)
                {
                    JOptionPane.showMessageDialog(null, "Cập nhật thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                    loadXuatKho();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại","Lỗi",JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Mã này không tồn tại nên không thể update","Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

