    public void loadNhapKho()
    {
        try {
            ArrayList<NhapKho> dsnk = NhapKhoDAO.layDsNhapKho();
            dtm.setRowCount(0);
            for(NhapKho nk : dsnk)
            {
                Vector<Object> vec = new Vector<Object>();
                vec.add(nk.getMaNhapKho());
                vec.add(nk.getMaNguyenLieu());
                vec.add(nk.getNgayNhap());
                vec.add(nk.getSoLuong());
                vec.add(nk.getThanhTien());
                dtm.addRow(vec);
            }
            tbNhapKho.setModel(dtm);
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }

