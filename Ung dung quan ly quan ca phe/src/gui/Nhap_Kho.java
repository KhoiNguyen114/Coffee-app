    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if(tfMaNhapKho.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(null, "Mã nhập kho không được để trống","Lỗi",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String ma = tfMaNhapKho.getText().trim();
        if(NhapKhoDAO.ktMa(ma))
        {
            int n = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn update?","Thông báo",JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if(n == JOptionPane.YES_OPTION)
            {
                NhapKho nk = new NhapKho();
                nk.setMaNhapKho(tfMaNhapKho.getText().trim());
                nk.setMaNguyenLieu(tfMaNL.getText().trim());
                nk.setNgayNhap(Date.valueOf(tfNgayNhap.getText().trim()));
                nk.setSoLuong(Integer.parseInt(tfSoLuong.getText()));
                nk.setThanhTien(Integer.parseInt(tfThanhTien.getText()));
                boolean kq = NhapKhoDAO.capNhatNhapKho(nk);
                if(kq)
                {
                    JOptionPane.showMessageDialog(null, "Cập nhật thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                    loadNhapKho();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Cập nhật thất bại","Lỗi",JOptionPane.ERROR_MESSAGE);
                }
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Mã này không tồn tại nên không thể update","Lỗi",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

