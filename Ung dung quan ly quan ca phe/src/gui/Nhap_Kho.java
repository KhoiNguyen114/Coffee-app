    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if(tfMaNhapKho.getText().length() == 0)
        {
            JOptionPane.showMessageDialog(null, "Mã nhập kho không được để trống","Lỗi",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(NhapKhoDAO.ktMa(tfMaNhapKho.getText().trim()))
        {
            JOptionPane.showMessageDialog(null, "Mã này đã tồn tại nên không thể thêm","Lỗi",JOptionPane.ERROR_MESSAGE);
            return;
        }
        NhapKho nk = new NhapKho();
        nk.setMaNhapKho(tfMaNhapKho.getText().trim());
        nk.setMaNguyenLieu(tfMaNL.getText().trim());
        nk.setNgayNhap(Date.valueOf(tfNgayNhap.getText().trim()));
        nk.setSoLuong(Integer.parseInt(tfSoLuong.getText()));
        nk.setThanhTien(Integer.parseInt(tfThanhTien.getText()));
        int n = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn thêm không?","Thông báo",JOptionPane.YES_NO_OPTION,JOptionPane.INFORMATION_MESSAGE);
        if(n == JOptionPane.YES_OPTION)
        {
            
            boolean kq = NhapKhoDAO.themNhapKho(nk);
            if(kq)
            {
                JOptionPane.showMessageDialog(null, "Thêm thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                loadNhapKho();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Thêm thất bại","Lỗi",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if(tbNhapKho.getSelectedRow() == -1)
        {
            JOptionPane.showMessageDialog(null, "Phải chọn 1 dòng để xóa","Lỗi",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String ma = tfMaNhapKho.getText();
        int n = JOptionPane.showConfirmDialog(null, "Bạn có chắc chắn muốn xóa?","Thông báo",JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if(n == JOptionPane.YES_OPTION)
        {
            boolean kq = NhapKhoDAO.xoaNhapKho(ma);
            if(kq)
            {
                JOptionPane.showMessageDialog(null, "Xóa thành công","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                loadNhapKho();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Xóa thất bại","Lỗi",JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

